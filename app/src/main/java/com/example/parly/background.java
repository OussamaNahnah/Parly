package com.example.parly;

public class background {
    String name;
    int photo,id;
    public background(String name,int photo,int id){
        this.name=name;
        this.photo=photo;
        this.id=id;
    }

    public String getName() {
        return name;
    }

    public int getPhoto() {
        return photo;
    }

    public int getId() {
        return id;
    }
}
